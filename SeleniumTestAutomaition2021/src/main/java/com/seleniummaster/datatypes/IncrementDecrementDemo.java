package com.seleniummaster.datatypes;

public class IncrementDecrementDemo {


    public static void main(String[] args) {

        int a=50;
        System.out.println(a);
        int b=100+(a++);
        System.out.println(b);
        System.out.println(a);
        int c=++a;
        System.out.println(c);
        System.out.println(a);




    }
}
